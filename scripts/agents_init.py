# ── Claude Code artifacts (from agents/sources/) ──────────────────────────────

def _source_for_subagent(name: str) -> Path:
    return SOURCES_ROOT / "subagents" / f"{name}.md"


def _source_for_command(name: str) -> Path:
    # name is like "/spec" — strip leading slash
    bare = name.lstrip("/")
    return SOURCES_ROOT / "commands" / f"{bare}.md"


def _source_for_skill(name: str) -> Path:
    return SOURCES_ROOT / "skills" / name / "SKILL.md"


def _dest_for_subagent(name: str) -> Path:
    return REPO_ROOT / ".claude" / "agents" / f"{name}.md"


def _dest_for_command(name: str) -> Path:
    bare = name.lstrip("/")
    return REPO_ROOT / ".claude" / "commands" / f"{bare}.md"


def _dest_for_skill(name: str) -> Path:
    return REPO_ROOT / ".claude" / "skills" / name / "SKILL.md"


def _with_generated_header(source_path: Path) -> str:
    body = source_path.read_text(encoding="utf-8")
    if body.startswith("---\n"):
        # Markdown with YAML frontmatter — inject header *after* frontmatter
        # so tools that parse frontmatter don't see the HTML comment as content.
        end = body.find("\n---\n", 4)
        if end != -1:
            split = end + len("\n---\n")
            return body[:split] + GENERATED_HEADER + body[split:]
    # Plain markdown — header on top.
    return GENERATED_HEADER + body


def collect_claude_outputs(cfg: dict) -> list[tuple[Path, str]]:
    """Build the full (dest_path, content) list for the .claude/ tree.

    Validates that every declared entry has a matching source file — fails loudly
    if a declaration in agents.yml doesn't have a source under agents/sources/.
    """
    surface = cfg.get("claude_code_surface") or {}
    outputs: list[tuple[Path, str]] = []
    missing: list[str] = []

    for s in surface.get("subagents") or []:
        src = _source_for_subagent(s["name"])
        if not src.exists():
            missing.append(f"subagent '{s['name']}': expected source at {src.relative_to(REPO_ROOT)}")
            continue
        outputs.append((_dest_for_subagent(s["name"]), _with_generated_header(src)))

    for s in surface.get("skills") or []:
        src = _source_for_skill(s["name"])
        if not src.exists():
            missing.append(f"skill '{s['name']}': expected source at {src.relative_to(REPO_ROOT)}")
            continue
        outputs.append((_dest_for_skill(s["name"]), _with_generated_header(src)))

    for c in surface.get("commands") or []:
        src = _source_for_command(c["name"])
        if not src.exists():
            missing.append(f"command '{c['name']}': expected source at {src.relative_to(REPO_ROOT)}")
            continue
        outputs.append((_dest_for_command(c["name"]), _with_generated_header(src)))

    if missing:
        msg = ["ERROR: agents.yml declares entries with no source file:"]
        msg.extend(f"  - {m}" for m in missing)
        msg.append("")
        msg.append("Create the source file or remove the declaration from agents.yml.")
        raise SystemExit("\n".join(msg))

    return outputs


# ── Drift / check mode ────────────────────────────────────────────────────────

def _diff_file(path: Path, expected: str) -> list[str]:
    """Return a unified-diff list of lines if the file differs from expected; empty otherwise."""
    actual = path.read_text(encoding="utf-8") if path.exists() else ""
    if actual == expected:
        return []
    rel = path.relative_to(REPO_ROOT)
    return list(difflib.unified_diff(
        actual.splitlines(keepends=True),
        expected.splitlines(keepends=True),
        fromfile=f"{rel} (on disk)",
        tofile=f"{rel} (expected)",
        n=2,
    ))


# ── Main ──────────────────────────────────────────────────────────────────────

def _build_outputs(cfg: dict) -> list[tuple[Path, str]]:
    return [
        (REPO_ROOT / ".github" / "copilot-instructions.md", generate_copilot(cfg)),
        (REPO_ROOT / ".cursor" / "rules" / "digithings.mdc", generate_cursor(cfg)),
        *collect_claude_outputs(cfg),
    ]


def main() -> int:
    ap = argparse.ArgumentParser(description="Generate agent-tool adapters from agents.yml.")
    ap.add_argument("--check", action="store_true",
                    help="Don't write; exit non-zero if any output would change (drift guard for CI).")
    args = ap.parse_args()

    if not CONFIG_PATH.exists():
        print(f"ERROR: {CONFIG_PATH} not found", file=sys.stderr)
        return 1

    cfg = load_config()
    outputs = _build_outputs(cfg)

    if args.check:
        drifted: list[Path] = []
        for path, content in outputs:
            diff = _diff_file(path, content)
            if diff:
                drifted.append(path)
                sys.stderr.writelines(diff)
        if drifted:
            print(
                f"\nagents-init --check: DRIFT — {len(drifted)} file(s) out of sync. Run `make agents-init`.",
                file=sys.stderr,
            )
            return 1
        print(f"agents-init --check: OK — {len(outputs)} files in sync with agents/sources/")
        return 0

    for path, content in outputs:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        print(f"  wrote  {path.relative_to(REPO_ROOT)}")

    _install_git_hooks()

    print(f"\nagents-init: OK — {len(outputs)} files generated from agents.yml + agents/sources/")
    return 0


def _install_git_hooks() -> None:
    """Copy scripts/hooks/pre-push.sh into .git/hooks/pre-push on this clone.

    Git hooks aren't tracked; this keeps every developer's clone consistent with
    the policy in scripts/hooks/. Silent if the source or .git/hooks is absent.
    """
    import shutil
    import stat

    src = REPO_ROOT / "scripts" / "hooks" / "pre-push.sh"
    dst = REPO_ROOT / ".git" / "hooks" / "pre-push"
    if not src.exists() or not dst.parent.exists():
        return
    shutil.copyfile(src, dst)
    dst.chmod(dst.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
    print(f"  wrote  {dst.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    sys.exit(main())
