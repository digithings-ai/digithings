#!/usr/bin/env python3
"""
agents_init.py — Generate platform adapter files from agents.yml.

Reads agents.yml at the repo root and the authoritative sources under
agents/sources/, then writes:

  - .claude/agents/*.md                     (Claude Code subagents)
  - .claude/skills/<name>/SKILL.md          (Claude Code skills)
  - .claude/commands/*.md                   (Claude Code slash commands)
  - .github/copilot-instructions.md         (GitHub Copilot workspace instructions)
  - .cursor/rules/digithings.mdc            (Cursor Agent rules, MDC format)

All outputs carry a "generated — do not edit" header. Edit the sources under
agents/sources/ and agents.yml, then re-run this script.

Run:  python3 scripts/agents_init.py            # regenerate
      python3 scripts/agents_init.py --check    # CI drift guard, non-zero on drift
Make: make agents-init
"""

from __future__ import annotations

import argparse
import difflib
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    print("ERROR: pyyaml is required. Install with: pip install pyyaml", file=sys.stderr)
    sys.exit(1)

REPO_ROOT = Path(__file__).resolve().parent.parent
CONFIG_PATH = REPO_ROOT / "agents.yml"
SOURCES_ROOT = REPO_ROOT / "agents" / "sources"

GENERATED_HEADER = "<!-- Generated by scripts/agents_init.py from agents.yml — do not edit directly. Edit the source under agents/sources/ and re-run `make agents-init`. -->\n"


def load_config() -> dict:
    with CONFIG_PATH.open() as f:
        return yaml.safe_load(f)


# ── Table / list helpers for Copilot + Cursor prose adapters ──────────────────

def _component_table(components: list[dict]) -> str:
    rows = ["| Component | Port | Description | Agent Guide |",
            "|-----------|------|-------------|-------------|"]
    for c in components:
        port = str(c["port"]) if c["port"] else "—"
        name = c["name"]
        desc = c["description"]
        link = f"[{name}/AGENTS.md]({c['agents_doc']})"
        rows.append(f"| `{name}` | {port} | {desc} | {link} |")
    return "\n".join(rows)


def _capabilities_list(capabilities: list[dict]) -> str:
    return "\n".join(
        f"- `make {cap['make_target']}` — {cap['description']} (`{cap['script']}`)"
        for cap in capabilities
    )


def _rules_list(rules: list[str]) -> str:
    return "\n".join(f"- {r}" for r in rules)


def _scoring_section(thresholds: dict) -> str:
    lines = [
        "| Dimension | Minimum | Rubric |",
        "|-----------|---------|--------|",
    ]
    rubric_files = {
        "security": "docs/scoring/SECURITY.md",
        "quality": "docs/scoring/QUALITY.md",
        "optimization": "docs/scoring/OPTIMIZATION.md",
        "accuracy": "docs/scoring/ACCURACY.md",
    }
    for dim, score in thresholds.items():
        rubric_link = f"[{rubric_files[dim]}]({rubric_files[dim]})"
        lines.append(f"| {dim.capitalize()} | ≥{score}/10 | {rubric_link} |")
    return "\n".join(lines)


def _human_gates_list(gates: list[str]) -> str:
    return "\n".join(f"- {g}" for g in gates)


def _agent_surface_prose(surface: dict) -> str:
    """Prose summary of subagents/skills/commands for Cursor + Copilot.

    Neither tool supports Claude's first-class subagents, skills, or slash commands,
    but they can still follow the same playbook if they know what each does and
    when to use it.
    """
    out = []
    subagents = surface.get("subagents") or []
    if subagents:
        out.append("**Subagents** (first-class in Claude Code; other tools should emulate the intent):\n")
        for s in subagents:
            out.append(f"- `{s['name']}` — {s['purpose']}")
        out.append("")

    skills = surface.get("skills") or []
    if skills:
        out.append("**Skills** (prompted workflows — invoke by name or when the trigger fits):\n")
        for s in skills:
            out.append(f"- `{s['name']}` — see `{s['path']}`")
        out.append("")

    commands = surface.get("commands") or []
    if commands:
        out.append("**Slash commands** (Claude Code) / prompted workflows (other tools):\n")
        for c in commands:
            out.append(f"- `{c['name']}` — see `{c['path']}`")
        out.append("")

    return "\n".join(out).rstrip() + "\n"


# ── Copilot instructions ──────────────────────────────────────────────────────

